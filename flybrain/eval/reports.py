"""Final-report builder used by ``flybrain-py report`` (PLAN.md §615).

Stitches the per-benchmark comparison tables, cherry-picked traces,
deliverables checklist, and a short discussion-skeleton into a single
Markdown document the operator can drop into ``docs/final_report.md``.

The intent is to have one command produce a report skeleton with all
the numbers filled in — humans still write the discussion sections,
but the metric-driven tables and links never go stale.
"""

from __future__ import annotations

import json
import math
from dataclasses import dataclass
from pathlib import Path
from typing import Any

from flybrain.eval.metrics import AggregateMetrics
from flybrain.eval.tables import markdown_table

_REPORT_TEMPLATE = """# FlyBrain Optimizer — research report

_Generated by `flybrain-py report`. Tables reflect a single benchmark
run; rerun the command after every refresh of `comparison_*.json`._

## 1. Setup

* Suite: `{suite_name}`
* Methods compared: **{num_methods}**
* Benchmarks: {benchmark_list}
* Total tasks evaluated: **{total_tasks}**
* Total spend (sum of `avg_cost_rub * num_tasks` across methods): {total_cost} ₽
* Solved tasks (verifier passed): **{total_solved}** / {total_tasks}
  ({total_solved_pct})

The pipeline is the one specified in `PLAN.md` §607-617:

```text
benchmarks/                      eval/
  loaders ─┐                       metrics ─┐
  runner ──┼─► <task>.trace.json ─►          ├─► comparison_*.{{md,json,csv}}
                                  tables ───┘    + report.md
                                  reports
```

## 2. Headline comparison (across all benchmarks)

{overall_table}

## 3. Per-benchmark breakdown

{per_benchmark_section}

## 4. Cost / quality summary

{cost_quality_section}

## 5. Cherry-picked traces

{trace_section}

## 6. Failure-mode breakdown

{failure_section}

## 7. Deliverables checklist (README §19)

{deliverables_section}

## 8. Experiments coverage (README §18)

{experiments_section}

## 9. Discussion (TODO — fill in after reading the numbers above)

* Where does FlyBrain prior + RL beat manual / fully-connected? When is
  hand-curated routing already optimal?
* Where does the cost / quality trade-off make a difference? Is the
  extra LLM cost on `random_sparse` worth the lower verifier score?
* What does `failed_component` (verifier output) say about the agents
  that limit the pipeline?
* Bridging gaps to the README §17 hypothesis ("brain-shaped routing
  generalises better than naive baselines on coding-heavy tasks").
"""


@dataclass(slots=True)
class CherryPick:
    """A single cherry-picked trace with the human-readable reason."""

    path: Path
    reason: str


@dataclass(slots=True)
class ReportInputs:
    """Everything needed to render the final report."""

    suite_name: str
    overall: list[AggregateMetrics]
    """One row per method, aggregated across all benchmarks."""

    per_benchmark: dict[str, list[AggregateMetrics]]
    """benchmark id → one row per method on that benchmark."""

    trace_paths: list[Path]
    """Cherry-picked trace files (max 3 by convention)."""

    cherry_picks: list[CherryPick] | None = None
    """Optional richer cherry-pick info; falls back to `trace_paths`."""


def _per_benchmark_section(per_benchmark: dict[str, list[AggregateMetrics]]) -> str:
    if not per_benchmark:
        return "_(no per-benchmark breakdown available)_\n"
    parts: list[str] = []
    for name in sorted(per_benchmark):
        parts.append(f"### {name}\n\n{markdown_table(per_benchmark[name])}\n")
    return "\n".join(parts)


def _trace_section(inputs: ReportInputs) -> str:
    cherry = inputs.cherry_picks
    if cherry:
        return "\n".join(f"* `{p.path}` — {p.reason}" for p in cherry) + "\n"
    if inputs.trace_paths:
        return "\n".join(f"* `{p}`" for p in inputs.trace_paths) + "\n"
    return "_(no cherry-picked traces selected)_\n"


def _cost_quality_section(overall: list[AggregateMetrics]) -> str:
    if not overall:
        return "_(no methods to summarise)_\n"
    lines = ["| Method | Success | Cost / task ₽ | Cost / solved ₽ |", "|---|---|---|---|"]
    for r in sorted(overall, key=lambda x: -x.success_rate):
        # Strict-JSON serialisation maps `float("inf")` to `null`, which loads
        # back as `None` (see `flybrain.eval.tables._fmt_value`); render those
        # the same way as live-rendered ∞ rather than crashing on `isfinite`.
        cps_value = r.cost_per_solved_rub
        if cps_value is None or not math.isfinite(cps_value):
            cps = "∞"
        else:
            cps = f"{cps_value:.3f}"
        lines.append(f"| `{r.name}` | {r.success_rate * 100:.1f}% | {r.avg_cost_rub:.3f} | {cps} |")
    return "\n".join(lines) + "\n"


def _failure_section(per_benchmark: dict[str, list[AggregateMetrics]]) -> str:
    if not per_benchmark:
        return "_(no per-benchmark breakdown available)_\n"
    rows: list[tuple[str, str, float]] = []
    for bench, methods in per_benchmark.items():
        for r in methods:
            if r.success_rate < 1.0:
                rows.append((r.name, bench, r.success_rate))
    if not rows:
        return "_All methods solved every benchmark in this run._\n"
    rows.sort(key=lambda t: t[2])
    parts = ["| Method | Benchmark | Success |", "|---|---|---|"]
    for name, bench, sr in rows[:12]:
        parts.append(f"| `{name}` | `{bench}` | {sr * 100:.1f}% |")
    if len(rows) > 12:
        parts.append(f"_(+{len(rows) - 12} more rows in `comparison_*.json`)_")
    return "\n".join(parts) + "\n"


