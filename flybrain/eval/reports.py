def _experiments_section() -> str:
    parts = []
    for label, body in _EXPERIMENTS:
        parts.append(f"### {label}\n\n{body}\n")
    return "\n".join(parts)


def render_report(inputs: ReportInputs) -> str:
    """Render `inputs` to a Markdown report string."""
    benchmarks = sorted(inputs.per_benchmark)
    total_tasks = sum(r.num_tasks for r in inputs.overall)
    total_cost = sum(r.avg_cost_rub * r.num_tasks for r in inputs.overall)
    total_solved = sum(round(r.success_rate * r.num_tasks) for r in inputs.overall)
    pct_solved = f"{(total_solved / total_tasks) * 100:.1f}%" if total_tasks else "—"
    return _REPORT_TEMPLATE.format(
        suite_name=inputs.suite_name,
        num_methods=len({r.name for r in inputs.overall}),
        benchmark_list=", ".join(f"`{b}`" for b in benchmarks) if benchmarks else "—",
        total_tasks=total_tasks,
        total_solved=total_solved,
        total_solved_pct=pct_solved,
        total_cost=f"{total_cost:.2f}",
        overall_table=markdown_table(inputs.overall),
        per_benchmark_section=_per_benchmark_section(inputs.per_benchmark),
        trace_section=_trace_section(inputs),
        cost_quality_section=_cost_quality_section(inputs.overall),
        failure_section=_failure_section(inputs.per_benchmark),
        deliverables_section=_deliverables_section(),
        experiments_section=_experiments_section(),
    )


def write_report(inputs: ReportInputs, path: Path) -> Path:
    """Render `inputs` and write the result to ``path`` (UTF-8)."""
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(render_report(inputs), encoding="utf-8")
    return path


def select_cherry_picks(out_dir: Path, *, max_picks: int = 3) -> list[CherryPick]:
    """Pick up to ``max_picks`` interesting traces from a benchmark output
    directory.

    Heuristic:

    * one solved trace with the highest verifier score,
    * one failed trace with the clearest ``failed_component``,
    * one trace where graph density spans a wide range across steps
      (good routing-diagnostics example).

    The function falls back gracefully if any category is empty.
    """
    candidates: list[tuple[Path, dict[str, Any]]] = []
    for trace_path in sorted(out_dir.rglob("*.trace.json")):
        try:
            data = json.loads(trace_path.read_text())
        except json.JSONDecodeError:
            continue
        candidates.append((trace_path, data))

    def _score(trace: dict[str, Any]) -> float:
        v = trace.get("verification") or {}
        return float(v.get("score") or 0.0)

    solved = [(p, d) for p, d in candidates if (d.get("verification") or {}).get("passed") is True]
    failed_with_component = [
        (p, d)
        for p, d in candidates
        if (d.get("verification") or {}).get("passed") is False
        and (d.get("verification") or {}).get("failed_component")
    ]
    routing = [
        (p, d) for p, d in candidates if any("graph_density" in s for s in d.get("steps") or [])
    ]

    picks: list[CherryPick] = []
    if solved:
        p, d = max(solved, key=lambda pd: _score(pd[1]))
        picks.append(
            CherryPick(
                path=p,
                reason=f"highest-scoring solved trace ({d.get('task_id')}, score={_score(d):.2f})",
            )
        )
    if failed_with_component:
        p, d = failed_with_component[0]
        v = d.get("verification") or {}
        picks.append(
            CherryPick(
                path=p,
                reason=f"failure mode = `{v.get('failed_component')}` ({d.get('task_id')})",
            )
        )
    if routing and len(picks) < max_picks:
        # Pick the trace with the widest density range over steps.
        def _density_range(d: dict[str, Any]) -> float:
            xs = [s.get("graph_density") for s in d.get("steps") or []]
            xs = [x for x in xs if isinstance(x, (int, float))]
            return float(max(xs) - min(xs)) if xs else 0.0

        p, d = max(routing, key=lambda pd: _density_range(pd[1]))
        if all(p != q.path for q in picks):
            picks.append(
                CherryPick(
                    path=p,
                    reason=f"widest graph-density range across steps ({d.get('task_id')})",
                )
            )

    return picks[:max_picks]


